using UnityEngine;
using System.Collections;

public class Projectile : MonoBehaviour
{
    //=======================================================================================================================================================/
    void Start()
    {

    }

    //=======================================================================================================================================================/
    void Update()
    {

    }

    //=======================================================================================================================================================/
    void OnTriggerEnter(Collider collider)
    {
        // Play Effect //
        if (collider.tag == "Enemy")
        {
            Destroy(collider.gameObject);
        }
        Destroy(this.gameObject);
    }

}
