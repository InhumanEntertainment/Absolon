using UnityEngine;
using System.Collections;

public class ScreenPause : MonoBehaviour
{
    //============================================================================================================================================================================================//
    public void Open()
    {
        Time.timeScale = 0f;
        //Audio.Music.Pause();
    }

    //============================================================================================================================================================================================//
    public void Close(){}

    //============================================================================================================================================================================================//
    void Update(){}

    //============================================================================================================================================================================================//
    public void Resume()
    {
        print("Frontend: Resume");

        Game.Instance.SetScreen("Game");
        Time.timeScale = 1;
        //Audio.Music.Play();
    }

    //============================================================================================================================================================================================//
    public void Restart()
    {
        Time.timeScale = 1;
        Audio.Music.Play();
    }

    //============================================================================================================================================================================================//
    public void Quit()
    {
        print("Frontend: Quit");

        Game.Instance.CleanupScene();
        Game.Instance.SetScreen("Menu");
    }

}
