using UnityEngine;
using System.Collections;

public class Menu : MonoBehaviour 
{
	public string Message = "";
	
	//=======================================================================================================================================================/
	void Update()
	{
		Vector3 mouse = Camera.main.ScreenToWorldPoint (Input.mousePosition);
		bool mouse_over = collider2D.OverlapPoint(mouse);
		if(mouse_over)
		{
			if(Input.GetMouseButtonDown(0))
			{
				print ("Menu: " + Message);
				SendMessage (Message);
			}
		}
	}

	//=======================================================================================================================================================/
	void Pause()
	{
        Game.Instance.Pause();
	}

    //=======================================================================================================================================================/
    void Resume()
    {
        Game.Instance.Resume();
    }

    //=======================================================================================================================================================/
    void Restart()
    {
        Game.Instance.Restart();
    }

    //=======================================================================================================================================================/
    void Play()
    {
        Game.Instance.Play();
    }
	
	//=======================================================================================================================================================/
	void Quit()
	{
        Game.Instance.Quit();
	}

    //=======================================================================================================================================================/
    void Facebook()
    {
        Game.Instance.Facebook();
    }

    //=======================================================================================================================================================/
    void Twitter()
    {
        Game.Instance.Twitter();
    }

    //=======================================================================================================================================================/
    void About()
    {
        Game.Instance.About();
    }
}
